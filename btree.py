    def __init__(self):
        self.left = None
        self.left = None
        self.value = None

    @property
    def left(self):
        return self.left

    @left.setter
    def left(self, left):
        self.left = left

    @property
    def right(self):
        return self.right

    @right.setter
    def right(self, right):
        self.right = right

    @property
    def value(self):
        return self.value

    @value.setter
    def value(self, left):
        self.left = left


